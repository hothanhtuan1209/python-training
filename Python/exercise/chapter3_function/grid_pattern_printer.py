def row(a):
    """
    Print a row element with the specified string 'a'.

    Parameters:
        a (str): The string representing the row element.
    """
    print(a)


def column(b):
    """
    Print a column element with the specified string 'b'.

    Parameters:
        b (str): The string representing the column element.
    """
    print(b)
    print(b)
    print(b)
    print(b)


